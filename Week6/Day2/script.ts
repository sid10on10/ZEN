class Pet {
  name: string;
  type: string;
  color: string;
  age: number;
  breed: string;
  history: string;
  Location: string;

  constructor(name,type,color,age,breed,history,Location) { 
      this.name = name;
      this.type = type;
      this.color = color;
      this.age = age;
      this.breed = breed;
      this.history = history;
      this.Location = Location;
  }
}

